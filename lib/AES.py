def shift_rows(state):
    for i in range(1, 4):
        state[Nb*i: Nb*(i+1)] = state[Nb*i + i: Nb*(i+1)] + \
            state[Nb*i: Nb*i + i]
    return state


def inv_shift_rows(state):
    for i in range(1, 4):
        state[Nb*i: Nb*(i+1)] = state[Nb*i + 4-i: Nb*(i+1)] + \
            state[Nb*i: Nb*i + 4-i]
    return state


def mix_columns(state):

    a = bytearray([0x02, 0x01, 0x01, 0x03])
