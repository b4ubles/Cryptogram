def key_expansion(w):

    for i in range(Nk, 4*(Nr+1)):
        tmp = [w[4*(i-1)+k] for k in range(4)]

        if i % Nk == 0:
            tmp = sub_bytes(rot_word(tmp))
            tmp = coef_add(tmp, Rcon(i/Nk))
        elif Nk > 6 and i % Nk == 4:
            tmp = sub_bytes(tmp)

        w += bytearray([(w[4*(i-Nk)+k] ^ tmp[k]) for k in range(4)])

    return w


def rot_word(w):
    return w[1:] + w[:1]


def coef_add(a, b):
    for i in range(4):
        a[i] ^= b[i]
    return a


def Rcon(i):
    R = bytearray([0x02, 0x00, 0x00, 0x00])
    if i == 1:
        R[0] = 0x01  # x^(1-1) = x^0 = 1
    elif i > 1:
        R[0] = 0x02
        i -= 1
        while i-1 > 0:
            R[0] = gmult(R[0], 0x02)
            i -= 1
    R[0] %= 256
    return R


def gmult(a, b):

    p = 0
    i = 0
    hbs = 0

    for i in range(8):
        if b & 1:
            p ^= a

        hbs = a & 0x80
        a <<= 1
        if (hbs):
            a ^= 0x1b  # 0000 0001 0001 1011
        b >>= 1

    return p % 256


def add_round_key(state, w, r):

    for c in range(Nb):
        for i in range(4):
            state[Nb*i+c] ^= w[4*Nb*r+4*c+i]

    return state


